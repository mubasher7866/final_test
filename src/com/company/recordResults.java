package com.company;

public class recordResults {
    String id;
    String name;
    String program;
    String course;
    String assignments;
    String quizzes;
    String midterm;
    String finals;

    public recordResults(String id, String name, String program, String course, String assignments, String quizzes, String midterm, String finals) {
        this.id = id;
        this.name = name;
        this.program = program;
        this.course = course;
        this.assignments = assignments;
        this.quizzes = quizzes;
        this.midterm = midterm;
        this.finals = finals;
    }
    
    
    
    
}
